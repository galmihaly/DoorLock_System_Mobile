package hu.unideb.inf.nfcapp;

public class MyLog {

    public int _gateId;
    public String _date;
    public int _logTypeId;

    public int get_gateId() {
        return _gateId;
    }

    public void set_gateId(int _gateId) {
        this._gateId = _gateId;
    }

    public String get_date() {
        return _date;
    }

    public void set_date(String _date) {
        this._date = _date;
    }

    public int get_logTypeId() {
        return _logTypeId;
    }

    public void set_logTypeId(int _logTypeId) {
        this._logTypeId = _logTypeId;
    }
}
